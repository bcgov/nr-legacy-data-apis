CREATE TABLE customers (
  customer_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_name VARCHAR2(100) NOT NULL,
  email_address VARCHAR2(255) NOT NULL UNIQUE,
  province_code CHAR(2) NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE field_sites (
  site_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_id NUMBER NOT NULL,
  site_name VARCHAR2(100) NOT NULL,
  site_type VARCHAR2(40) NOT NULL,
  status VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
  location MDSYS.SDO_GEOMETRY NOT NULL,
  location_geojson VARCHAR2(512) NOT NULL,
  installed_on DATE NOT NULL,
  CONSTRAINT chk_field_sites_location_geojson
    CHECK (location_geojson IS JSON),
  CONSTRAINT fk_field_sites_customer
    FOREIGN KEY (customer_id) REFERENCES customers(customer_id)
);

CREATE TABLE work_orders (
  work_order_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_id NUMBER NOT NULL,
  site_id NUMBER NOT NULL,
  summary VARCHAR2(200) NOT NULL,
  priority VARCHAR2(20) NOT NULL,
  status VARCHAR2(20) NOT NULL,
  requested_at TIMESTAMP NOT NULL,
  completed_at TIMESTAMP,
  CONSTRAINT fk_work_orders_customer
    FOREIGN KEY (customer_id) REFERENCES customers(customer_id),
  CONSTRAINT fk_work_orders_site
    FOREIGN KEY (site_id) REFERENCES field_sites(site_id)
);

