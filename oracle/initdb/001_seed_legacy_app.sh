WHENEVER SQLERROR EXIT SQL.SQLCODE

ALTER SESSION SET CONTAINER=FREEPDB1;
ALTER USER ${app_user} QUOTA UNLIMITED ON USERS;
GRANT EXECUTE ON MDSYS.SDO_GEOMETRY TO ${sql_identifier};
GRANT EXECUTE ON MDSYS.SDO_POINT_TYPE TO ${sql_identifier};
GRANT EXECUTE ON MDSYS.SDO_DIM_ARRAY TO ${sql_identifier};
GRANT EXECUTE ON MDSYS.SDO_DIM_ELEMENT TO ${sql_identifier};

CONNECT ${app_user}/${app_password}@//localhost/FREEPDB1

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE work_orders CASCADE CONSTRAINTS';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE != -942 THEN
      RAISE;
    END IF;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE field_sites CASCADE CONSTRAINTS';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE != -942 THEN
      RAISE;
    END IF;
