#!/bin/bash
set -euo pipefail

app_user="${APP_USER:?APP_USER must be set}"
app_password="${APP_USER_PASSWORD:?APP_USER_PASSWORD must be set}"

sql_identifier="$(printf '%s' "$app_user" | tr '[:lower:]' '[:upper:]')"

