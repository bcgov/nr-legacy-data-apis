    IF SQLCODE != -942 THEN
      RAISE;
    END IF;
END;
/

BEGIN
  DELETE FROM USER_SDO_GEOM_METADATA WHERE table_name = 'FIELD_SITES' AND column_name = 'LOCATION';
EXCEPTION
  WHEN OTHERS THEN
    NULL;
END;
